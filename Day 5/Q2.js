function hiren() {
    var a = document.getElementById("h").value;

    if (a >= 90) {
        alert(`A grade`)
    }
    else if (a >= 80 && a <= 89) {
        alert(`B grade`)

    }
    else if (a >= 70 && a <= 79) {
        alert(`C grade`)

    }
    else if (a >= 60 && a <= 69) {
        alert(`D grade`)

    }
    else {
        alert(`E grade`)

    }




}